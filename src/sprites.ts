/** 繪製地板與路徑 Tile */
export function drawTile(
  ctx: CanvasRenderingContext2D,
  theme: string,
  isPath: boolean,
  x: number,
  y: number,
  scale: number = 1,
  cellX: number = 0,
  cellY: number = 0
): void {
  // 標準中式關卡使用事件驅動的程序化日間地形，避免 16px 紋理在
  // 80×40 地圖上形成重複條紋。這段只在 tile cache 重建時執行。
  if (theme === 'chinese') {
    const size = 16 * scale;
    const hash = Math.abs((cellX * 73856093) ^ (cellY * 19349663));

    if (isPath) {
      const pathColors = ['#D8BC82', '#CFAD72', '#E2C991', '#C69B64'];
      ctx.fillStyle = pathColors[hash % pathColors.length];
      ctx.fillRect(x, y, size, size);
      ctx.fillStyle = 'rgba(92, 61, 36, 0.16)';
      if ((hash & 1) === 0) ctx.fillRect(x, y, size, Math.max(1, scale));
      if ((hash & 2) === 0) ctx.fillRect(x, y, Math.max(1, scale), size);
      ctx.fillStyle = 'rgba(255, 243, 196, 0.34)';
      const pebble = Math.max(1, scale);
      ctx.fillRect(x + ((hash % 9) + 2) * scale, y + (((hash >> 4) % 9) + 2) * scale, pebble * 2, pebble);
    } else {
      // 日間草地採三層像素紋理：底色、明暗草叢、花草／碎石點綴。
      ctx.fillStyle = '#96BE67';
      ctx.fillRect(x, y, size, size);
      ctx.fillStyle = (hash % 4 === 0) ? '#8DB45F' : '#A3C970';
      ctx.fillRect(x + (hash % 5) * scale, y + ((hash >> 3) % 5) * scale, 5 * scale, 3 * scale);
      ctx.fillStyle = '#6C9C4F';
      const tuftX = x + ((hash % 11) + 2) * scale;
      const tuftY = y + (((hash >> 5) % 10) + 3) * scale;
      ctx.fillRect(tuftX, tuftY, Math.max(1, scale), 3 * scale);
      ctx.fillRect(tuftX + scale, tuftY + scale, 2 * scale, Math.max(1, scale));
      if (hash % 6 === 0) {
        ctx.fillStyle = '#E8D87A';
        ctx.fillRect(tuftX + 3 * scale, tuftY, Math.max(1, scale), Math.max(1, scale));
        ctx.fillStyle = '#FFF3C4';
        ctx.fillRect(tuftX + 4 * scale, tuftY + scale, Math.max(1, scale), Math.max(1, scale));
      }
      if (hash % 11 === 0) {
        ctx.fillStyle = '#B8AE93';
        ctx.fillRect(x + 11 * scale, y + 11 * scale, 2 * scale, scale);
      }
    }
    return;
  }

  let key = `tile_${theme}_`;
  if (isPath) {
    key += 'path_0';
  } else {
    key += 'ground_';
    if (theme === 'scifi') {
      // 科幻主題地面有兩個變體，利用網格坐標進行雜湊，產生更自然的交錯分布
      const variant = (cellX * 17 + cellY * 23) % 2;
      key += variant;
    } else {
      // 其他主題以 20% 機率分發裝飾變體，其餘 80% 為普通地面，保持自然點綴感
      const rand = (cellX * 17 + cellY * 23) % 10;
      const variant = (rand < 2) ? 1 : 0;
      key += variant;
    }
  }

  const cvs = spriteCache.get(key);
  if (cvs) {
    ctx.drawImage(cvs, x, y, cvs.width * scale, cvs.height * scale);
  } else {
    // 降級純色安全 Fallback (如果快取尚未生成)
    ctx.fillStyle = isPath ? '#1e293b' : '#020617';
    ctx.fillRect(x, y, 16 * scale, 16 * scale);
  }
}

